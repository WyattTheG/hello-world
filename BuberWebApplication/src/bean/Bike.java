package bean;

public class Bike {

	
	private String ownerName,location;  
	private int ownerId, bikeId, rating, DesiredBikeId;
	private double price, timeIn, timeOut;
	  
	public String getOwnerName() {  
	    return ownerName;  
	}  
	  
	public void setOwnerName(String ownerName) {  
	    this.ownerName = ownerName;  
	}  
	
	public String getLocation() {  
	    return location;  
	}  
	  
	public void setLocation(String location) {  
	    this.location = location;  
	}  
	
	public int getOwnerId() {  
	    return ownerId;  
	}  
	  
	public void setOwnerId(int ownerId) {  
	    this.ownerId = ownerId;  
	}  
	
	public int getBikeId() {  
	    return bikeId;  
	}  
	  
	public void setBikeId(int bikeId) {  
	    this.bikeId = bikeId;  
	}  
	
	public int getRating() {  
	    return rating;  
	}  
	  
	public void setRating(int rating) {  
	    this.rating = rating;  
	}  
	
	public double getPrice() {  
	    return price;  
	}  
	  
	public void setPrice(double price) {  
	    this.price = price;  
	}  
	  
	public double getTimeIn() {  
	    return timeIn;  
	}  
	  
	public void setTimeIn(double timeIn) {  
	    this.timeIn = timeIn;  
	} 
	
	public double getTimeOut() {  
	    return timeOut;  
	}  
	  
	public void setTimeOut(double timeOut) {  
	    this.timeOut = timeOut;  
	} 
	
	public int getDesiredBikeId() {  
	    return DesiredBikeId;  
	}  
	  
	public void setDesiredBikeId(int DesiredBikeId) {  
	    this.DesiredBikeId = DesiredBikeId;  
	} 
	
	
}
